                        <div class="col-md-6">
                            <h2 class="section-title">¿Qué es un Biodigestor?</h2>
                            <p>
                                La biodigestión es un proceso biológico que transforma residuos orgánicos en biogás y un fertilizante llamado biol. Este proceso se realiza en un biodigestor, un contenedor hermético donde se depositan los residuos orgánicos.</p>
                            <p>
                                En el interior del biodigestor, las bacterias y hongos presentes en los desechos orgánicos degradan la materia orgánica en ausencia de oxígeno, lo que se conoce como digestión anaeróbica. El resultado de este proceso es el biogás y el biol, que se puede utilizar como fertilizante y regenerador de suelos.</p>
                            <p>
                                El biogás se puede utilizar para: Cocción de alimentos, Calefacción, Refrigeración, Generación de vapor, Generación eléctrica.</p>
